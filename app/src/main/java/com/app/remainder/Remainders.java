package com.app.remainder;

public class Remainders {
    String date;
    int remid;
    String time;
    String note;

    public Remainders() {
    }

    public int getRemid() {
        return remid;
    }

    public void setRemid(int remid) {
        this.remid = remid;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
